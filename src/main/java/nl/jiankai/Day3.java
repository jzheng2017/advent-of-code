    private static long giveTreeCount(int right, int down) {
        return traverse(right, down);
    }

    private static int traverse(int right, int down) {
        String[] arr = s.split("\n");
        char[][] arr2 = new char[arr.length][arr[0].length()];

        for (int i = 0; i < arr.length; i++) {
            arr2[i] = arr[i].toCharArray();
        }

        int treeCount = 0;
        final int bottom = arr.length - 1;
        final int maxRowSize = arr[0].length();
        int currentRow = 0;
        int currentColumn = 1;

        while (currentRow < bottom) {
            currentColumn += right;
            if (currentColumn > maxRowSize) {
                currentColumn = currentColumn - maxRowSize;
            }
            currentRow += down;

            if (arr2[currentRow][currentColumn - 1] == '#') {
                treeCount++;
            }
        }
        return treeCount;
    }


